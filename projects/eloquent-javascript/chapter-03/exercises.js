function min(num1, num2) {
return Math.min(num1, num2); // use and return math min with the two parameters to get the minimum value between two numbers
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
if(x % 2 === 0 || x == 0){ // if x remainder 2 equals zero or is equal to zero
return true; // return true
} else if (x % 2 === 1 || x == -1){ // else if x remainder 2 equals one or is equal to negative 1
  return false; // return false
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) { 
  var number = 0 // counter for character occurence equals zero
for (var i = 0; i < string.length; i++){ // for loop to go through string
  if (string[i] == character){ // if the iteration of the character in the string is equal to the character we're looking for
    number += 1; // add one to our counter
  }
} return number; // return our counter
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var number = 0 // counter for character occurence equals zero
for (var i = 0; i < string.length; i++){ // for loop to go through string
  if (string[i] == "B"){ // if the iteration of the string is equal to capital B
    number += 1; // add one to our counter 
  }
} return number; // return our counter
}

